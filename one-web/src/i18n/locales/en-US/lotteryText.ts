export const translateEnglishText = (value: string) => {
  const leading = value.match(/^\s*/)?.[0] || '';
  const trailing = value.match(/\s*$/)?.[0] || '';
  const trimmed = value.trim();
  if (!trimmed) return value;
  const exact = lotterySeasonTranslations[trimmed]
    ?? lotteryResearchTranslations[trimmed]
    ?? lotteryDecisionTranslations[trimmed]
    ?? lotteryPredictionTranslations[trimmed]
    ?? lotteryInsightTranslations[trimmed]
    ?? lotteryGovernanceTranslations[trimmed]
    ?? lotteryReviewTranslations[trimmed]
    ?? lotteryExecutionTranslations[trimmed]
    ?? lotteryPageTranslations[trimmed]
    ?? lotteryExactTranslations[trimmed];
  if (exact) return `${leading}${exact}${trailing}`;
  let translated = trimmed;
  for (const [pattern, replacement] of lotteryPhraseTranslations) {
    translated = translated.replace(pattern, replacement);
  }
  translated = translated
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/，/g, ', ')
    .replace(/。/g, '.')
    .replace(/：/g, ': ')
    .replace(/；/g, '; ')
    .replace(/？/g, '?')
    .replace(/！/g, '!')
    .replace(/、/g, ', ')
    .replace(/[ \t]{2,}/g, ' ')
    .trim();
  return `${leading}${translated}${trailing}`;
};

const chineseSourcePattern = /[\u3400-\u9fff]/u;

/** Prevent the phrase-level compatibility layer from emitting mixed-language copy. */
export const translateCompleteEnglishText = (value: string) => {
  const translated = translateEnglishText(value);
  return chineseSourcePattern.test(translated) ? value : translated;
};
